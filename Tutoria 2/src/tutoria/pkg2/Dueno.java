package tutoria.pkg2;

public class Dueno {
    private String nombre;
    private int telefono;

    public Dueno(String nombre, int telefono) {
        this.nombre = nombre;
        this.telefono = telefono;
    }

    public String getNombre() {
        return nombre;
    }

    public int getTelefono() {
        return telefono;
    }
}
